//Aula sobre condicionais

var numero = 15;

//Teste numero par
if(numero % 2 == 0){
    console.log('É par!');
}
else{
    console.log('É impar!');
}

//Numero divisível por 5
if(numero % 5 == 0){
    console.log('O número é divisível por 5!');
}
else{
    console.log('O número não é divisível!');
}